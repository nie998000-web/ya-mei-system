create table if not exists public.employees (
  id bigint generated by default as identity primary key,
  name text,
  phone text,
  store text,
  role text,
  note text,
  base_salary numeric default 0,
  social_security_allowance numeric default 0,
  full_attendance_bonus numeric default 0,
  seniority_salary numeric default 0,
  entry_date date,
  is_active boolean default true,
  is_technical_department boolean default false,
  salary_plan_type text,
  today_followups numeric default 0,
  today_appointments numeric default 0,
  today_arrivals numeric default 0,
  today_deals numeric default 0,
  today_sales numeric default 0,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

