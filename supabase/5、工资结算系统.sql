  add column if not exists updated_at timestamptz default now();

create unique index if not exists idx_employee_daily_stats_date_employee_id on public.employee_daily_stats (date, employee_id);
create index if not exists idx_employee_daily_stats_date on public.employee_daily_stats (date);
create index if not exists idx_employee_daily_stats_store on public.employee_daily_stats (store);
create index if not exists idx_employee_daily_stats_employee_id on public.employee_daily_stats (employee_id);

create table if not exists public.project_commission_settings (
  id bigint generated by default as identity primary key,
  project_name text,
  category text,
  manual_commission numeric default 0,
  duration_minutes numeric,
  unit text default '次',
  is_active boolean default true,
  remark text,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

alter table public.project_commission_settings
  add column if not exists project_name text,
  add column if not exists category text,
  add column if not exists manual_commission numeric default 0,
  add column if not exists duration_minutes numeric,
